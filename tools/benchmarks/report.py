"""Markdown report generator for the M92 benchmark harness."""
from __future__ import annotations

from pathlib import Path
from typing import Iterable

from .runner import DatasetRunSummary


def _mb(n_bytes: int | float | None) -> str:
    if n_bytes is None:
        return "n/a"
    return f"{float(n_bytes) / 1e6:.2f} MB"


def _ratio(format_bytes: int | None, baseline_bytes: int) -> str:
    if format_bytes is None or baseline_bytes <= 0:
        return "n/a"
    return f"{format_bytes / baseline_bytes:.3f}"


def _seconds(s: float | None) -> str:
    if s is None:
        return "n/a"
    return f"{s:.2f}"


def _throughput_mbps(n_bytes: int | None, seconds: float | None) -> str:
    if n_bytes is None or seconds is None or seconds <= 0:
        return "n/a"
    return f"{(float(n_bytes) / 1e6) / seconds:.1f}"


def render(summaries: Iterable[DatasetRunSummary], title: str) -> str:
    lines: list[str] = [
        f"# {title}",
        "",
        "Generated by `tools/benchmarks/cli.py run --report ...`. ",
        "Distinct from `tools/perf/` (read-path latency profiling).",
        "",
    ]

    for summary in summaries:
        baseline = summary.bam_input_bytes
        host = summary.host
        lines.append(f"## Dataset: `{summary.dataset}`")
        lines.append("")
        lines.append(f"- BAM input size: **{_mb(baseline)}** (compression-ratio baseline)")
        lines.append(
            f"- Host: {host.get('hostname', '?')} — {host.get('platform', '?')}"
        )
        lines.append(
            f"- Tooling: samtools `{host.get('samtools', '?')}`, "
            f"ttio `{host.get('ttio', '?')}`"
        )
        lines.append("")
        lines.append(
            "| Format | Compressed size | vs BAM | Compress (s) | "
            "Compress MB/s | Decompress (s) | Decompress MB/s | Notes |"
        )
        lines.append("|---|---|---|---|---|---|---|---|")
        for fmt, ops in summary.formats.items():
            c = ops.get("compress", {})
            d = ops.get("decompress", {})
            if "error" in c:
                lines.append(
                    f"| `{fmt}` | error | — | — | — | — | — | {c['error']} |"
                )
                continue
            cmp_size = c.get("output_size_bytes")
            cmp_wall = c.get("wall_seconds")
            dec_wall = d.get("wall_seconds")
            note = c.get("notes", "") or ""
            if "error" in d:
                note = f"{note} (decompress: {d['error']})".strip()
            row = (
                f"| `{fmt}` "
                f"| {_mb(cmp_size)} "
                f"| {_ratio(cmp_size, baseline)} "
                f"| {_seconds(cmp_wall)} "
                f"| {_throughput_mbps(baseline, cmp_wall)} "
                f"| {_seconds(dec_wall)} "
                f"| {_throughput_mbps(d.get('output_size_bytes'), dec_wall)} "
                f"| {note} |"
            )
            lines.append(row)
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def write(summaries: Iterable[DatasetRunSummary], path: Path, title: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(render(summaries, title))
